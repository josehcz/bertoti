public class Cliente{
    
    public String nome;
    public String pet;
    public String especie;
    public String pet_raca;
    public String pedido;

    public Cliente (String nome, String pet, String especie, String pet_raca , String pedido)
    {
        nome = nome;
        pet = pet;
        especie = especie;
        pet_raca = pet_raca;
        pedido = pedido;
    }
}